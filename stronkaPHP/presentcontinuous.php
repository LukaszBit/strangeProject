<main><h2><b>Present Continuous</b></h2><br/>
    <p>Budowa czasu Present Continuous<br/><br/>

        Czas Present Continuous składa się z następujących elementów:</p><br/>
    <p class="skladnia">podmiot + to be (am/is/are) + czasownik z końcówką -ing</p>
    <p>Odmiana to be jest prosta – podajemy ją niżej, opisujemy także jak utworzyć czasownik z końcówką -ing.

        Rozpiszmy przykłady na poszczególne osoby:</p><br/>
    <table>
        <tr><td></td><td colspan="2">liczba pojedyncza </td><td colspan="2">liczba mnoga</td></tr>
        <tr><td>1 osoba</td><td>I am</td><td rowspan="3">walking<br/>doing<br/>going<br/>having<br/>working</td><td>We are</td><td rowspan="3">eating<br/>writing<br/>talking<br/>jumping<br/>waiting</td></tr>
        <tr><td>2 osoba</td><td>You are</td><td>You are</td></tr>
        <tr><td>3 osoba</td><td>He/She/It is</td><td>They are</td></tr>
    </table><br/>
    <p>Przykłady:<br/>
        Tom is writing a letter. --- Tomek pisze list. (w tej chwili)<br/>
        We are waiting for a taxi. --- Czekamy na taksówkę.<br/><br/>
        Najpierw mamy osobę, która wykonuje czynność Tom, następnie trzecią formę czasownika być is.
        Następnie dodajemy do czasownika głównego pisać write końcówkę -ing i dopełnienie - w tym wypadku słowo list a letter.
        Pierwsze zdanie w Present Continuous gotowe!<br/><br/>
    </p>
    <p>
        Zwróćmy uwagę, że niektóre czasowniki po dodaniu końcówki wyglądają nieco inaczej. 
        Zapamiętajmy zasady pisowni czasowników z końcówką -ing:<br/><br/></p>
    <ul>
        <li>czasowniki zakończone na -e, -e na końcu czasownika znika:<br/><p>dance » dancing</p></li> 
        <li>w czasowniku jednosylabowym, gdzie na końcu jest spółgłoska + samogłoska + spółgłoska podwajamy ostatnią spółgłoskę *, podwajanie nie dotyczy spółgłosek w i x.
            <p>run » running</p>
        </li>
        <li>w czasownikach dwusylabowych, gdzie na końcu jest spółgłoska + samogłoska + spółgłoska, a akcent pada na drugą sylabę, podwajamy ostatnią spółgłoskę
            ** Dla porównania: czasownik happen też ma dwie sylaby, a na końcu spółgłoskę + samogłoskę + spółgłoskę (p + e + n ), ale ponieważ akcentujemy w nim pierwszą sylabę, nie podwoimy ostatniej spółgłoski przed dodaniem końcówki -ing. 
            W tym przypadku otrzymujemy formę happening. 
            <p>
                begin » beginning<br/>
                forget » forgetting
            </p>
        </li>
        <li>
            czasowniki kończące się na -ie zmieniają pisownię z -ie na -y: 
            <p>
                lie » lying
            </p>
        </li>
    </ul><br/><br/>
    <p class="naglowek">Present Continuous - pytania i przeczenia.</p><br/><br/>
    <p>
        Pytania w Present Continuous<br/><br/>

        Tworzenie pytań jest banalnie proste - wystarczy tylko zamienić ze sobą miejscami czasownik pomocniczy z podmiotem zdania:<br/>
        Is Tom writing a letter? --- Czy Tomek pisze list? (w tej chwili)<br/><br/>
        Przeczenia w Present Continuous<br/><br/>

        Tworzenie przeczeń w Present Continuous jest bardzo proste. Potrzebne nam będzie dodatkowe słowo: not (czyli nie):<br/>
        Tom is not writing a letter. --- Tomek nie pisze listu.<br/><br/>

        W języku potocznym najczęściej łączymy dwa słówka:<br/>
        is not » isn't<br/>
        are not » aren't
    </p><br/><br/>
    <p>
        Czasu Present Continuous używamy w następujących sytuacjach:<br/><br/>

        1.Opisując czynności, które dzieją się w momencie mówienia o nich. Możemy dodatkowo użyć słówek mówiących, że czynność jest wykonywana w tej chwili.<br/>
        2.Present Continuous także do opisu czynności, które nie muszą trwać dokładnie w chwili mówienia o nich, ale trwają dłuższy czas (włączając w to chwilę obecną).
        Czynność opisana w tym czasie będzie zazwyczaj mieć charakter tymczasowy.<br/>
        3.Jeśli coś zaplanowaliśmy, to także możemy wyrazić to w Present Continuous.<br/>
        4.Jeśli opisujemy czynność powtarzającą się lecz nie jest to "stała" cecha człowieka czy rzeczy.<br/><br/>

        Oraz słówka określające częstotliwość:<br/>
        at present --- obecnie<br/>
        nowadays --- w obecnych czasach<br/>
        today --- dziś<br/>
        tonight --- dziś wieczorem/ w nocy<br/>
        these days --- w tych dniach, aktualnie<br/><br/><br/>
    </p>

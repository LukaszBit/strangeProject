<main><h2><b>Present Simple</b></h2><br/>
    <p>Czas Present Simple jest najczęściej używanym czasem teraźniejszym w języku angielskim.
        Stosujemy go w bardzo wielu sytuacjach.Można śmiało powiedzieć, że jeśli jesteśmy wprawni w gestykulacji i znamy trochę słownictwa, 
        Present Simple umożliwi nam komunikację, która wychodzi daleko poza jego kontekst.<br/><br/>
    <h4>Budowa czasu:</h4><br/>
    <p>Present Simple jest bardzo prosty w tworzeniu. Potrzeba tylko czasownika w podstawowej formie i osoby wykonującej czynność.
        Czas Present Simple składa się z następujących elementów:</p><br/>
    <p class="skladnia">podmiot + czasownik w formie podstawowej *</p><br/><br/>
    <p>`s` w trzeciej osobie liczby pojedynczej <br/>
        * Żeby jednak nie było tak łatwo, jest jedna mała rzecz, o której zawsze trzeba pamiętać w tym czasie:<br/>
        W trzeciej osobie liczby pojedynczej do czasownika musimy dodać końcówkę -s<br/><br/>
        Czasami jednak trzeba dodać -es zamiast -s<br/>
        Bywa że trudno wymówić czasownik, który kończy się na 'syczącą" spółgłoskę, gdybyśmy dodali do niego tylko -s. Końcówka -es ułatwia wymowę.<br/>
        Jeśli czasownik kończy się na literkę -y, a przed nią jest spółgłoska, to w trzeciej osobie zamiast -y wstawiamy -ies.
    </p><br/><br/>
    <p> W Present Simple głównym czasownikiem może też być to be.<br/>
        Oto formy czasownika to be w czasie teraźniejszym dla wszystkich osób:<br/></p>
    <table>
        <tr><td></td><td>liczba pojedyncza </td><td>liczba mnoga</td></tr>
        <tr><td>1 osoba</td><td>I am</td><td>We are</td></tr>
        <tr><td>2 osoba</td><td>You are</td><td>You are</td></tr>
        <tr><td>3 osoba</td><td>He/She/It is</td><td>They are</td></tr>
    </table>
    <p><br/>I na koniec bardzo ważny wyjątek:<br/>
        I have » he/she/it has ---(mieć, ma)</p><br/><br/>
    <p class="naglowek">Present Simple - pytania i przeczenia.</p><br/><br/>
    <p>
        Pytania w Present Simple<br/>

        Przy tworzeniu pytań pojawia się dodatkowe słówko (operator). Słowo to nie ma tutaj swojego znaczenia, pełni rolę wyłączne pomocniczą. To słówko to do. W trzeciej osobie pojawia się końcówka -s i otrzymujemy does.
        Uwaga: w trzeciej osobie w pytaniu nie dodajemy końcówki -s do czasownika głównego - otrzymuje ją operator. Wyjątkiem są pytania o podmiot (o nich trochę niżej).<br/><br/>

        Aby utworzyć pytanie ogólne, zaczynamy od operatora:<br/>
        Do you like apples? --- Lubisz jabłka?<br/>
        Does she smoke? --- Czy ona pali?<br/><br/>
        Możemy także sprecyzować nasze pytania (pytania szczegółowe), zaczynając pytanie od słówka pytającego:<br/>
        How do you get to work? --- Jak się dostajesz do pracy?<br/><br/>
        Uwaga! W pytaniach o podmiot w formie twierdzącej nie używamy operatora. Gdy pytamy kto lub co, czasownik będzie w 3. osobie liczby pojedynczej, czyli z końcówką -e(s):<br/>
        Who knows the truth? --- Kto zna prawdę?<br/>
        Gdy forma pytania sugeruje podmiot w liczbie mnogiej (How many...?), czasownik będzie w formie, której używamy dla liczby mnogiej, czyli bez końcówki -e(s):<br/>
        How many people live here? --- Ile osób tu mieszka?<br/><br/>
        Pytania o podmiot w formie przeczącej będą miały operator + not + bezokolicznik lub never + czasownik w 3. osobie liczby pojedynczej:<br/>
        Who doesn't want to know the truth? --- Kto nie chce znać prawdy?<br/>
        Gdy pytanie w formie przeczącej sugeruje podmiot w liczbie mnogiej (How many...?), użyjemy don't + bezokolicznika:<br/>
        How many people don't have a bank account? --- Ile osób nie ma konta w banku?<br/><br/>
        Pytania z czasownikiem to be<br/>
        Jeżeli głównym czasownikiem jest to be, musimy zapomnieć o do/does.<br/>
        Pytania tworzymy przez inwersję:<br/>
        Are they ever on time? --- Czy oni kiedykolwiek są na czas?<br/>
        Is Tom free on Monday afternoons? --- Czy Tom jest wolny w poniedziałki po południu?<br/><br/>
        Przeczenia w Present Simple<br/>
        Tutaj także używamy operatora do/does, do którego dodajemy słówko not i przeczenie gotowe:<br/>
        I don't like milk. --- Nie lubię mleka.<br/>
        He doesn't smoke. --- On nie pali.<br/>
        W języku potocznym najczęściej łączymy operator i not:<br/>
        do not » don't<br/>
        does not » doesn't<br/><br/>
        Mike doesn't read books very often. --- Mike nie czyta książek za często.<br/>
        I don't go to work on Sundays. --- Nie chodzę do pracy w niedziele.<br/><br/>
        Przeczenia z czasownikiem to be<br/>
        Tutaj potrzebne nam będzie dodatkowe słowo: playnot (czyli nie), które dodajemy do to be:<br/>
        Tom is not free on Monday afternoons. --- Tomek nie jest wolny w poniedziałkowe popołudnia.<br/>
        W języku potocznym najczęściej łączymy dwa słówka:<br/>
        is not » isn't<br/>
        are not » aren't<br/><br/>
        Czasu Present Simple używamy w następujących sytuacjach:<br/>
        1.Mówiąc o czynnościach typowych, rutynowych, powtarzających się. Opisujemy cechy i stany ludzi i przedmiotów.<br/>
        2.Opisujemy rzeczy, które zawsze są prawdziwe; zjawiska.<br/>
        3.Czynności dziejące się przez dłuższy czas.<br/>
        4.Opisywanie, streszczanie filmów, książek, itp.<br/><br/>

    <p> Co to jest przedimek?<br/>
        Przedimki sprawiają uczącym się wiele kłopotów, pewnie także dlatego, że nie mają odpowiednika w języku polskim...
        Najkrócej mówiąc, przedimki to krótkie słówka określające jakiś rzeczownik - no dobrze, to trochę mało wyjaśniająca definicja. 
        Ale zaraz będzie jaśniej. Po pierwsze, poznajmy te przedimki - na całe szczęście są tylko trzy: a, an , the.<br/><br/>
        Przedimki nieokreślone<br/>
        Dwa pierwsze - a, an - to przedimki nieokreślone, the to przedimek określony.<br/><br/>
        Teraz najgorsza część - lista zasad, gdzie i jak wstawiać odpowiedni przedimek.
        Podstawowa zasada:<br/>
        Przedimek nieokreślony wstawiamy przed rzeczownikiem w liczbie pojedynczej, 
        kiedy po raz pierwszy o czymś mówimy, natomiast przedimek określony wrzucamy przed rzeczownik, 
        jeśli wiadomo, o czym mówimy, czyli gdy już określiliśmy daną rzecz, osobę, itd.<br/>
        Prześledźmy to wszystko na przykładach.<br/>
        Idziemy przez park i nagle widzimy przed nami psa, powiemy:<br/>
        This is a dog. --- To jest pies.<br/>
        Czyli jest to jakiś bliżej nieokreślony pies - wstawiamy w takim razie przedimek nieokreślony. Żeby lepiej to zrozumieć spróbujmy słowko a tłumaczyć sobie jako jakiś, jakaś, jakieś.
        Skoro już wiemy, o jakiego psa chodzi (już określiliśmy go), w kolejnych zdaniach, jeśli będziemy go chcieli opisać, użyjemy przedimka określonego:<br/>
        The dog is very small. --- Ten pies jest malutki.<br/>
        Jak widać, nie mówimy już o jakimś psie, tylko dokładnie o tym spotkanym w parku - ten pies jest malutki - jakieś inne psy są większe czy wręcz olbrzymie, ale nas interesuje ten jeden - ten jest malutki. 
        Wstawiamy w takim razie przedimek określony the przed rzeczownikiem pies.<br/>
        Ok, ale co z trzecim przedimkiem - an? Otóż używamy go dokładnie w tych samych przypadkach co przedimka a - jednak bardzo ważne jest rozróżnienie, kiedy użyć a a kiedy an.<br/><br/>
        Kiedy a? Kiedy an?<br/>
        Przedimek a wstawiamy przed wyrazem, którego wymowa zaczyna się od spółgłoski, natomiast an wybierzemy, jeśli przy wymowie pierwsza jest samogłoska.
        Bardzo ważne jest to, że chodzi o wymowę wyrazu - nie jego pisownię!<br/>
        an hour --- godzina(wymowa hour zaczyna się od samogłoski - przedimek an)<br/><br/>
